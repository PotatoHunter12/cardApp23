    <p><strong>Raise</strong> — A player who thinks he has a good hand (or who wants the other players to think he has a 
        good hand) may increase the wager required to continue playing.
    </p>
    <p><strong>Fold</strong> — A player who thinks his hand is not good enough to win and who does not want to wager the 
        increased amount may lay down his cards. He cannot win the hand, but he also will not lose any more chips.
    </p>
    <p><strong>Call</strong> — Once a player has raised the stakes, each player must decide whether to raise the stakes 
        again, to give in and fold his hand, or to call, which means to equal the amount wagered by the player who raised.
    </p>
    <p><strong>Check</strong> — If no one has increased the wager required to continue, a player may stand pat by checking,
        or passing on his option to bet.
    </p>
