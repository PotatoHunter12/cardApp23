    <h1>Poker rules</h1>

    <p>In Draw Poker, each player is dealt five cards, and a round of betting ensues. The remaining players then may 
        attempt to improve their hands by trading as many as three cards for a new three from the deck. If a player 
        has an Ace, he may trade all four of his other cards if he so desires.
    </p>
