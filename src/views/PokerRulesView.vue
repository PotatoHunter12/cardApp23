        seven cards are dealt to each player. Players attempt to form the best five-card poker hand possible (see below). 
        For every poker game, the same hierarchy of hands exists, and the better hands are rarer and more difficult to 
        achieve than the lesser hands.
    </p>
    <p>Individual cards are ranked from best to worst. The rank of a card often breaks the tie if two players achieve the 
